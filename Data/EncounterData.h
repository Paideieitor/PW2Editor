#ifndef _ENCOUNTER_DATA_H
#define _ENCOUNTER_DATA_H

#include "Data/Data.h"

#define ENCOUNTER_NULL -1

enum EncounterType
{
	LAND_SINGLES,
	LAND_DOUBLES,
	LAND_SPECIAL,
	SURF,
	SURF_SPECIAL,
	FISHING,
	FISHING_SPECIAL,
	UNKNOWN_ENCOUNTER,
	ENCOUNTER_TYPE_MAX,
};
#define ENCOUNTER_RATE_START 0
#define ENCOUNTER_RATE_SIZE ENCOUNTER_TYPE_MAX

#define SINGLES_COUNT 12
#define DOUBLES_COUNT 12
#define SPECIAL_COUNT 12
#define SURF_COUNT  5
#define SURF_SPECIAL_COUNT  5
#define FISHING_COUNT  5
#define FISHING_SPECIAL_COUNT  5
enum EncounterField
{
	ENCOUNTER_SPECIES,
	ENCOUNTER_FORM,
	ENCOUNTER_MIN_LEVEL,
	ENCOUNTER_MAX_LEVEL,
	ENCOUNTER_MAX,
};

#define ENCOUNTER_SINGLES_START (ENCOUNTER_RATE_START + ENCOUNTER_RATE_SIZE)
#define ENCOUNTER_SINGLES_SIZE (SINGLES_COUNT * ENCOUNTER_MAX)

#define ENCOUNTER_DOUBLES_START (ENCOUNTER_SINGLES_START + ENCOUNTER_SINGLES_SIZE)
#define ENCOUNTER_DOUBLES_SIZE (DOUBLES_COUNT * ENCOUNTER_MAX)

#define ENCOUNTER_SPECIAL_START (ENCOUNTER_DOUBLES_START + ENCOUNTER_DOUBLES_SIZE)
#define ENCOUNTER_SPECIAL_SIZE (SPECIAL_COUNT * ENCOUNTER_MAX)

#define ENCOUNTER_SURF_START (ENCOUNTER_SPECIAL_START + ENCOUNTER_SPECIAL_SIZE)
#define ENCOUNTER_SURF_SIZE (SURF_COUNT * ENCOUNTER_MAX)

#define ENCOUNTER_SURF_SPECIAL_START (ENCOUNTER_SURF_START + ENCOUNTER_SURF_SIZE)
#define ENCOUNTER_SURF_SPECIAL_SIZE (SURF_SPECIAL_COUNT * ENCOUNTER_MAX)

#define ENCOUNTER_FISHING_START (ENCOUNTER_SURF_SPECIAL_START + ENCOUNTER_SURF_SPECIAL_SIZE)
#define ENCOUNTER_FISHING_SIZE (FISHING_COUNT * ENCOUNTER_MAX)

#define ENCOUNTER_FISHING_SPECIAL_START (ENCOUNTER_FISHING_START + ENCOUNTER_FISHING_SIZE)
#define ENCOUNTER_FISHING_SPECIAL_SIZE (FISHING_SPECIAL_COUNT * ENCOUNTER_MAX)

enum Season
{
	SUMMER,
	SPRING,
	FALL,
	WINTER,
	SEASON_MAX,
};
#define SEASON_START(season) (SEASON_SIZE * season)
#define SEASON_SIZE (ENCOUNTER_FISHING_SPECIAL_START + ENCOUNTER_FISHING_SPECIAL_SIZE)

#define ENCOUNTER_RATE(season, type) ((SEASON_SIZE * season) + ENCOUNTER_RATE_START + type)

#define ENCOUNTER_SINGLES_FIELD(season, field) (SEASON_START(season) + ENCOUNTER_SINGLES_START + field)
#define ENCOUNTER_DOUBLES_FIELD(season, field) (SEASON_START(season) + ENCOUNTER_DOUBLES_START + field)
#define ENCOUNTER_SPECIAL_FIELD(season, field) (SEASON_START(season) + ENCOUNTER_SPECIAL_START + field)
#define ENCOUNTER_SURF_FIELD(season, field) (SEASON_START(season) + ENCOUNTER_SURF_START + field)
#define ENCOUNTER_SURF_SPECIAL_FIELD(season, field) (SEASON_START(season) + ENCOUNTER_SURF_SPECIAL_START + field)
#define ENCOUNTER_FISHING_FIELD(season, field) (SEASON_START(season) + ENCOUNTER_FISHING_START + field)
#define ENCOUNTER_FISHING_SPECIAL_FIELD(season, field) (SEASON_START(season) + ENCOUNTER_FISHING_SPECIAL_START + field)

#define ENCOUNTER_SLOTS (SINGLES_COUNT + DOUBLES_COUNT + SPECIAL_COUNT + SURF_COUNT + SURF_SPECIAL_COUNT + FISHING_COUNT + FISHING_SPECIAL_COUNT)
#define ENCOUNTER_SIZE (SEASON_SIZE * SEASON_MAX)

class EncounterData : public Data
{
public:

	virtual void GenerateSections(const vector<string>& narcPaths) override;
};

#endif // _ENCOUNTER_DATA_H
